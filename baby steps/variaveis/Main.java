
/* VARIAVEIS
*
* Em programação variaveis sao como ingredientes em uma receita;
* Uma variavel tem um nome e um valor;
* O nome da variavel é usado para se referir ao seu valor;
* No exemplo abaixo, a variavel 'farinha' tem valor 2
*
*/

public class Main {
	public static void main (String[] args){
		int farinha = 2;
		System.out.println("A receita requer " + farinha + " xícaras de farinha");
	}
}